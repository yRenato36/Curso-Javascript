let texto = "texto de teste";

//chartAt, encontra o caracter no indice fornecido
console.log(texto.charAt(0));

//indexOf, indica o indice que começa a palavra fornecida
console.log(texto.indexOf('texto'));

/*slice(), extrai um ou mais caracteres de um texto
0: conta a partir do indice zero da string
5: até onde vai essa contagem
*/
console.log(texto.slice(0, 5));
